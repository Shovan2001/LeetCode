class Solution {
  public:
    // Function which returns the  root of the flattened linked list.
    Node *mergeTwoSortedList(Node *l1,Node *l2)
    {
        if(l1==NULL)
        return l2;
        if(l2==NULL)
        return l1;
        
        Node *res;
        if(l1->data<l2->data)
        {
            res=l1;
            res->bottom=mergeTwoSortedList(l1->bottom,l2);
        }
        else
        {
            res=l2;
            res->bottom=mergeTwoSortedList(l1,l2->bottom);
        }
        
        res->next=NULL;
        
        return res;
    }
    Node *flatten(Node *root) {
        // Your code here
        
        if(root==NULL)
        return NULL;
        
        Node *head1=flatten(root->next);
        Node *head2=root;
        
        Node *res=mergeTwoSortedList(head1,head2);
        
        return res;
    }
};


